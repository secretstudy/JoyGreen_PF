use JoyGreen_PF

SET ANSI_NULLS ON --사용하면 NULL 값을 처리하는 방식이 변경됩니다. 
				  --비교 및 연산 시 NULL 값이 포함된 경우 예상대로 동작하도록 강제합니다.
GO

SET QUOTED_IDENTIFIER ON --예약어나 특수 문자를 포함하는 식별자를 큰따옴표로 묶어 사용할 수 있게 됩니다.
GO

CREATE PROCEDURE [dbo].[USP_CHECK_PW]
		@STORE_IDX	INT	=	0	
as

SET NOCOUNT ON
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED 

BEGIN		

	declare @pwd nvarchar(1000)
	set @pwd = '84D89877F0D4041EFB6BF91A16F0248F2FD573E6AF05C19F96BEDB9F882F7882'

	SELECT	@pwd = PW
	  FROM	DBO.TBL_JG_STORE
	 WHERE	STORE_IDX			=	@STORE_IDX

	SELECT	isnull(@pwd, '84D89877F0D4041EFB6BF91A16F0248F2FD573E6AF05C19F96BEDB9F882F7882') as PWD

END


GO


